	{
		if (Object != nullptr && !Object->m_IsDestroyed)
		{
			++Object->m_ReferenceCount;
		}
	}


	/////////////////////////////////////////////////////////////////////////////
	// @ 레퍼런스 카운트 감소.
	/////////////////////////////////////////////////////////////////////////////
	VOID DecreaseReference(AObject Object)
	{
		if (Object != nullptr && !Object->m_IsDestroyed)
		{
			--Object->m_ReferenceCount;

			if (Object->m_ReferenceCount <= 0)
			{
				Object->m_IsDestroyed = true;
			}
		}
	}


