#include "AObject.h"
#include "AType.h"
#include "AVariable.h"


namespace AcroEngine
{
	/////////////////////////////////////////////////////////////////////////////
	// @ 생성.
	/////////////////////////////////////////////////////////////////////////////
	Object::Object()
	{
		m_ObjectID = 0;
		m_IsDestroying = false;
	}

	/////////////////////////////////////////////////////////////////////////////
	// @ 소멸.
	/////////////////////////////////////////////////////////////////////////////
	Object::~Object()
	{

	}

	/////////////////////////////////////////////////////////////////////////////
	// @
	/////////////////////////////////////////////////////////////////////////////
	AString Object::ToString()
	{
		return A_INSTANTIATE(String);
	}

	/////////////////////////////////////////////////////////////////////////////
	// @ 동일한 오브젝트인지 확인.
	/////////////////////////////////////////////////////////////////////////////
	bool8 Object::Equals(AObject Target)
	{
		if (Target.IsNull())
			return false;

		return m_ObjectID == Target->GetObjectID();
	}

	/////////////////////////////////////////////////////////////////////////////
	// @ 오브젝트 아이디 반환.
	/////////////////////////////////////////////////////////////////////////////
	unit64 Object::GetObjectID()
	{
		return m_ObjectID;
	}
}