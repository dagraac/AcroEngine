#include "AcroCore.h"


namespace AcroCore
{
	/////////////////////////////////////////////////////////////////////////////
	// @ 공통 전역 변수.
	/////////////////////////////////////////////////////////////////////////////
	extern std::chrono::system_clock::time_point g_StartTime;
	extern float32 g_DeltaTime;


	/////////////////////////////////////////////////////////////////////////////
	// @ 어플리케이션이 시작된 후 부터 흐른 시간.
	/////////////////////////////////////////////////////////////////////////////
	float32 GetApplicationTime()
	{
		std::chrono::duration<float32> duration = std::chrono::system_clock::now() - g_StartTime;
		return duration.count();
	}


	/////////////////////////////////////////////////////////////////////////////
	// @ 어플리케이션의 프레임 사이 시간 간격.
	/////////////////////////////////////////////////////////////////////////////
	float32 GetApplicationDeltaTime()
	{
		return g_DeltaTime;
	}


	/////////////////////////////////////////////////////////////////////////////
	// @ 플랫폼 별 어플리케이션 처리.
	/////////////////////////////////////////////////////////////////////////////
#include "ApplicationAndroid.cpp"
#include "ApplicationIOS.cpp"
#include "ApplicationWin32.cpp"
}